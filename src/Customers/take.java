    private void bdnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdnsearchActionPerformed
        // TODO add your handling code here:
        
        String strSearch = "select * from receipt where ";
        if(rdoNumber.isSelected()){
            strSearch += "id";
        }
          else if(rdocustid.isSelected()){
            strSearch += "idcustomer";
        }
        else if(rdoName.isSelected()){
            strSearch += "customername";
        }
          else if(itemid.isSelected()){
            strSearch += "productid";
        }
        else if(item.isSelected()){
            strSearch += "productname";
        }
        
        else if(rdoemp.isSelected()){
            strSearch += "employee";
        }
       
       // else if(rdodetails.isSelected()){
      //      strSearch += "storename";
        //}
        else{
            strSearch += "date";
        }
        strSearch += " like '%"+  txtSearch.getText()  +"%'";
        ct.getCustomRows(strSearch, tbltake);
    }//GEN-LAST:event_bdnsearchActionPerformed

