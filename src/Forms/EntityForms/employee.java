    private void cleardata(){
        TheTools.clearText(this);
      
        txtid.setText(emp.getAutoNumber());
        bdnadd.setEnabled(true);
        bdnupdate.setEnabled(false);
        bdndelete.setEnabled(false);
        txtname.requestFocus();
        
         txtSearch.setText("0");
       txtsalary.setText("0");
       txtfullname.setText("الاسم المتواجد فى البطاقة");
    }
    
    
    private void setValue(){
    emp.setId(Integer.parseInt(txtid.getText()));
    emp.setEmployeename(txtname.getText());
    emp.setFullname(txtfullname.getText());
    emp.setPhone(txtphone.getText());
    emp.setJob(txtjob.getText());
    emp.setAddress(txtaddress.getText());
     emp.setAbsence(Integer.parseInt(txtabsence.getText()));
    emp.setSalary(Integer.parseInt(txtsalary.getText()));
   
    
}
    
    private void tbldeptMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tbldeptMouseClicked
        try{
        int row = tbldept.getSelectedRow();
        
        
        String id =(String) tbldept.getValueAt(row , 0);
        String name = tbldept.getValueAt(row, 1).toString();
        String phone =(String) tbldept.getValueAt(row, 2);
        String job =(String) tbldept.getValueAt(row, 3);
        String address =(String) tbldept.getValueAt(row , 4);
        String absence =(String) tbldept.getValueAt(row, 5);
         String salary =(String) tbldept.getValueAt(row, 6);
         String fullname =(String) tbldept.getValueAt(row, 7);


        txtid.setText(id);
        txtname.setText(name);
        txtfullname.setText(fullname);
        txtphone.setText(phone);
        txtjob.setText(job);
        txtsalary.setText(salary);
        txtabsence.setText(absence);
        txtaddress.setText(address);
        
        bdnadd.setEnabled(false);
        bdnupdate.setEnabled(true);
        bdndelete.setEnabled(true);
        }
        catch(Exception ex){
            
            TheTools.msgBox(ex.getMessage());
        }
    }//GEN-LAST:event_tbldeptMouseClicked
 
