    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        // TODO add your handling code here:

        try{
            int row = tbl.getSelectedRow();

          
            String iditem = tbl.getValueAt(row, 0).toString();
            String item =(String) tbl.getValueAt(row, 1);
            String weight =(String) tbl.getValueAt(row, 2);

            String exp =(String) tbl.getValueAt(row, 3);
            String imp =(String) tbl.getValueAt(row, 4);
            String balance =(String) tbl.getValueAt(row, 5);

            tbl.clearSelection();
             tbl2.clearSelection();
            
          
            txtid1.setText(iditem);
            txtname.setText(item);
            txtweight.setText(weight);

            txtexp.setText(exp);
           
            txtimp.setText(imp);
            txtbalance.setText(balance);

          
        }
        catch(Exception ex){

            TheTools.msgBox(ex.getMessage());
        }

    }//GEN-LAST:event_tblMouseClicked
