    private void bdnsearchActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdnsearchActionPerformed
        // TODO add your handling code here:
          // TODO add your handling code here:

        String strSearch = "select * from storeproduct where ";
        String strSearch2 = "select * from `productbalance` where ";
        if(rdoNumber.isSelected()){
            strSearch += "iditem";
             strSearch2 += "id";
        }
        else if(rdoName.isSelected()){
            strSearch += "item";
             strSearch2 += "item";
        }else {
        strSearch += "balance";
             strSearch2 += "balance";
        }

        strSearch += " like '%"+  txtSearch.getText()  +"%'";
        strSearch2 += " like '%"+  txtSearch.getText()  +"%'";
        st.getCustomRows(strSearch, tbl);
        st.getCustomRows(strSearch2, tbl2);
    }//GEN-LAST:event_bdnsearchActionPerformed

