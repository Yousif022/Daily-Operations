    private void tblMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblMouseClicked
        // TODO add your handling code here:

        try{
            int row = tbl.getSelectedRow();

            String id = tbl.getValueAt(row, 0).toString();
            String name =(String) tbl.getValueAt(row, 1);
            String take =(String) tbl.getValueAt(row, 2);

            String pay =(String) tbl.getValueAt(row, 3);
            String receiptno =(String) tbl.getValueAt(row, 4);
            String collectionno =(String) tbl.getValueAt(row, 5);
            String date =(String) tbl.getValueAt(row, 6);
           
               
            txtid.setText(id);
            txtname.setText(name);
          txttake.setText(take);
         txtpay.setText(pay);
         txtreceiptno.setText(receiptno);
           txtcolno.setText(collectionno);
              txtdate.setText(date);
          
        }
        catch(Exception ex){

            TheTools.msgBox(ex.getMessage());
        }
    }//GEN-LAST:event_tblMouseClicked

