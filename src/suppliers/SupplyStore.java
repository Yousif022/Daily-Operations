    private void bdnsearch1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bdnsearch1ActionPerformed
        // TODO add your handling code here:

        String strSearch = "select * from supplystatement where ";
        if(rdocolnum.isSelected()){
            strSearch += "paymentsnum";
        }
        else if(rdogoodsnum.isSelected()){
            strSearch += "receiptnum";
        }
        else if(rdosupname.isSelected()){
            strSearch += "name";
        }
        else if(rdopay.isSelected()){
            strSearch += "pay";
        }
        
        else if(rdopay.isSelected()){
            strSearch += "take";
        }

        // else if(rdodetails.isSelected()){
            //      strSearch += "storename";
            //}
        else{
            strSearch += "date";
        }
        strSearch += " like '%"+  txtSearch.getText()  +"%'";
        ct.getCustomRows(strSearch, tbl);
    }//GEN-LAST:event_bdnsearch1ActionPerformed

